DARK_RED = "\033[31m"

YELLOW = "\033[93m"
DARK_YELLOW = "\033[33m"

BLUE = "\033[94m"
DARK_BLUE = "\033[34m"

MAGENTA = "\033[95m"
CYAN = "\033[96m"

RESET = "\033[0m"
